# --- Display Form Data (GET and POST) ---
print('<div class="card"><h2>Submitted Data (GET & POST)</h2>')
if not form.keys():
    print("<p>No GET or POST data was submitted.</p>")
else:
    print("<table><tr><th>Key</th><th>Value</th></tr>")
    for key in form.keys():
        # Use html.escape in Python 3.8+ for better security
        value = cgi.escape(form.getvalue(key))
        print(f"<tr><td>{cgi.escape(key)}</td><td>{value}</td></tr>")
    print("</table>")
print('</div>')


# --- Display Environment Variables ---
print('<h2>CGI Environment Variables</h2>')
